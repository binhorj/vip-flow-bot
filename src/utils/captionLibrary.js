/**
 * Pega legendas aleatórias de uma categoria
 */
function getRandomCaptions(category, count = 5) {
  const captions = captionLibrary[category];

  if (!captions) {
    return [];
  }

  if (Array.isArray(captions)) {
    // Array: embaralha e pega os primeiros N
    const shuffled = [...captions].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
  }

  // Objeto: retorna values
  return Object.values(captions).slice(0, count);
}

/**
 * Pega todas as categorias disponíveis
 */
function getCategories() {
  return Object.keys(captionLibrary).filter(
    key => key !== 'completos'
  );
}

/**
 * Combina legendas para criar variações
 */
function combineCaptions(category1, category2) {
  const cap1 = getRandomCaptions(category1, 1)[0];
  const cap2 = getRandomCaptions(category2, 1)[0];

  return {
    strategy: `${category1} + ${category2}`,
    caption: `${cap1}\n\n${cap2}`,
    components: [cap1, cap2]
  };
}

/**
 * Sugestão inteligente baseada em nível de chamada
 */
function getSuggestionByCallLevel(level) {
  const strategies = {
    suave: ['curiosidade', 'exclusividade'],
    medio: ['curiosidade', 'oferta', 'novidade'],
    direto: ['urgencia', 'oferta', 'cta']
  };

  const categories = strategies[level] || strategies.suave;
  const category = categories[Math.floor(Math.random() * categories.length)];

  return getRandomCaptions(category, 1)[0];
}

/**
 * Conta total de legendas
 */
function getTotalCaptions() {
  let total = 0;
  for (const key in captionLibrary) {
    if (key !== 'completos') {
      const item = captionLibrary[key];
      total += Array.isArray(item) ? item.length : Object.keys(item).length;
    }
  }
  return total;
}

